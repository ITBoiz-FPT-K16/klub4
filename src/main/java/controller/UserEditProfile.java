    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("new-name");
        String avatar = request.getParameter("image-url");
        String address = request.getParameter("new-address");
        String date = request.getParameter("new-DOB");
        Date dOB = Date.valueOf(date);
        HttpSession session = request.getSession();
        User currentUser = (User) session.getAttribute("userData");
        User newUser = new User(currentUser.getUserID(), name, currentUser.getEmail(), currentUser.getPassword(), dOB, address, currentUser.getOTP(), avatar);
        UserDAO.updateUser(newUser);
        session.setAttribute("userData", newUser);
        request.getRequestDispatcher("user-view-profile.jsp").forward(request, response);
    }

